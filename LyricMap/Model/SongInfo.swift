import Foundation

struct SongInfo {
    // Id of the song
    let songId: Int = 0
    
    // Name of the song
    let songName: String
    
    // Name of the song's album
    let albumName: String
    
    // Link of the album's cover
    let albumImageUrl: String
    
    // Name of the song's artist
    let artistName: String
}
